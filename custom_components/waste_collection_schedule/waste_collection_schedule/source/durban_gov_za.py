def _south_africa_public_holidays(year: int) -> set[date]:
    """Return observed South African public holidays for the given year."""
    holidays: set[date] = []

    def observe(day: date) -> date:
        if day.weekday() == 5:
            return day + timedelta(days=2)
        if day.weekday() == 6:
            return day + timedelta(days=1)
        return day

    fixed = [
        (1, 1),
        (3, 21),
        (4, 27),
        (5, 1),
        (6, 16),
        (8, 9),
        (9, 24),
        (12, 16),
        (12, 25),
        (12, 26),
    ]
    for month, day in fixed:
        holidays.append(observe(date(year, month, day)))

    good_friday = easter(year) - timedelta(days=2)
    family_day = easter(year) + timedelta(days=1)
    holidays.extend([good_friday, family_day])

    return set(holidays)


def _is_public_holiday(day: date) -> bool:
    return day in _south_africa_public_holidays(day.year) or day in (
        _south_africa_public_holidays(day.year - 1) if day.month == 1 else set()
    )


def _next_weekday(ref: date, weekday: int) -> date:
    days_ahead = weekday - ref.weekday()
    if days_ahead < 0:
        days_ahead += 7
    return ref + timedelta(days=days_ahead)


class Source:
    def __init__(
        self,
        region: str,
        area: str,
        recycling_in_even_week: bool = True,
    ):
        region_key = region.strip().lower()
        area_key = area.strip().lower()

        if region_key not in COLLECTION_AREAS:
            raise SourceArgumentNotFoundWithSuggestions(
                "region",
                region,
                sorted(REGION_TITLES.keys()) or sorted(COLLECTION_AREAS.keys()),
            )

        region_areas = COLLECTION_AREAS[region_key]
        if area_key not in region_areas:
            raise SourceArgumentNotFoundWithSuggestions(
                "area",
                area,
                sorted(region_areas.keys()),
            )

        self._weekday = int(region_areas[area_key]["weekday"])
        self._recycling_in_even_week = recycling_in_even_week

    def fetch(self) -> list[Collection]:
        today = date.today()
        entries: list[Collection] = []
        collection_date = _next_weekday(today - timedelta(days=7), self._weekday)

        while collection_date < today + timedelta(days=365):
            entries.append(
                Collection(
                    date=collection_date,
                    t="General Waste",
                    icon=ICON_MAP["General Waste"],
                )
            )

            weeks_since_ref = (collection_date - REFERENCE_MONDAY).days // 7
            is_even_week = weeks_since_ref % 2 == 0
            is_recycling_week = is_even_week == self._recycling_in_even_week

            if is_recycling_week and not _is_public_holiday(collection_date):
                entries.append(
                    Collection(
                        date=collection_date,
                        t="Recycling",
                        icon=ICON_MAP["Recycling"],
                    )
                )

            collection_date += timedelta(days=7)

        return entries
